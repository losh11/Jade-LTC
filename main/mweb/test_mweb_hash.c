int test_mweb_hash(void)
{
    failures = 0;

    /* Tag + empty data */
    check_hash("hashed('A', empty)", MWEB_TAG_ADDRESS, NULL, 0, HASHED_A_EMPTY);
    check_hash("hashed('B', empty)", MWEB_TAG_BLIND, NULL, 0, HASHED_B_EMPTY);
    check_hash("hashed('D', empty)", MWEB_TAG_DERIVE, NULL, 0, HASHED_D_EMPTY);
    check_hash("hashed('O', empty)", MWEB_TAG_OUTKEY, NULL, 0, HASHED_O_EMPTY);

    /* Tag + 32 zero bytes */
    uint8_t zeros[32] = {0};
    check_hash("hashed('B', zeros32)", MWEB_TAG_BLIND, zeros, 32, HASHED_B_ZEROS32);
    check_hash("hashed('O', zeros32)", MWEB_TAG_OUTKEY, zeros, 32, HASHED_O_ZEROS32);

    /* Subaddress modifier: m_i = BLAKE3('A' || index_le32 || scan_key)
     * spend_key + m_0 matches mweb_compat_test.go expected spend_key_0 */
    uint8_t buf[4 + 32];
    memcpy(buf + 4, SCAN_KEY, 32);

    /* index = 0 */
    buf[0] = 0; buf[1] = 0; buf[2] = 0; buf[3] = 0;
    check_hash("hashed('A', idx=0||scan_key)", MWEB_TAG_ADDRESS, buf, 36, HASHED_A_IDX0_SCAN);

    /* index = 1 */
    buf[0] = 1; buf[1] = 0; buf[2] = 0; buf[3] = 0;
    check_hash("hashed('A', idx=1||scan_key)", MWEB_TAG_ADDRESS, buf, 36, HASHED_A_IDX1_SCAN);

    /* index = 10 */
    buf[0] = 10; buf[1] = 0; buf[2] = 0; buf[3] = 0;
    check_hash("hashed('A', idx=10||scan_key)", MWEB_TAG_ADDRESS, buf, 36, HASHED_A_IDX10_SCAN);

    printf("\nmweb_hash: %d tests, %d failures\n", 9, failures);
    return failures;
}
