/*
 * Test vectors for mweb_hashed (BLAKE3 tagged hashing).
 *
 * Vectors generated from the reference BLAKE3 C implementation
 * (m_i = BLAKE3('A' || index_le32 || scan_key),
 * spend_key_i = spend_key + m_i matches expected values).
 */
#include "mweb_hash.h"

#include <stdio.h>
#include <string.h>

static int failures = 0;

static void check_hash(const char* name,
                       uint8_t tag, const uint8_t* data, size_t data_len,
                       const uint8_t expected[32])
{
    uint8_t output[32];
    mweb_hashed(tag, data, data_len, output);
    if (memcmp(output, expected, 32) != 0) {
        printf("FAIL: %s\n  got:  ", name);
        for (int i = 0; i < 32; i++) printf("%02x", output[i]);
        printf("\n  want: ");
        for (int i = 0; i < 32; i++) printf("%02x", expected[i]);
        printf("\n");
        failures++;
    } else {
        printf("PASS: %s\n", name);
    }
}

