/* --- Test helpers --- */

static void print_hex(const char* label, const uint8_t* data, size_t len)
{
    printf("  %s: ", label);
    for (size_t i = 0; i < len; i++) printf("%02x", data[i]);
    printf("\n");
}

/*
 * Test that signing produces the exact expected signature bytes.
 * This catches regressions in the QR normalization — if the R.y check
 * changes from QR to even/odd, the R.x and s values will differ.
 */
static void test_fixed_vector(const char* name,
                              const uint8_t secret_key[32],
                              const uint8_t* msg, size_t msg_len,
                              const uint8_t expected_sig[64])
{
    uint8_t signature[64];

    if (!mweb_schnorr_sign(secret_key, msg, msg_len, signature)) {
        printf("FAIL: %s — sign failed\n", name);
        failures++;
        return;
    }

    if (memcmp(signature, expected_sig, 64) != 0) {
        printf("FAIL: %s — signature mismatch\n", name);
        print_hex("got ", signature, 64);
        print_hex("want", expected_sig, 64);
        failures++;
        return;
    }

    printf("PASS: %s\n", name);
}

/*
 * Test that signing the same key+message twice yields identical output.
 * MWEB Schnorr uses deterministic nonce: k = SHA256(sk || msg).
 */
static void test_deterministic(void)
{
    uint8_t sig1[64], sig2[64];

    if (!mweb_schnorr_sign(SCAN_KEY, MSG_ZERO, 32, sig1) ||
        !mweb_schnorr_sign(SCAN_KEY, MSG_ZERO, 32, sig2)) {
        printf("FAIL: deterministic — sign failed\n");
        failures++;
        return;
    }

    if (memcmp(sig1, sig2, 64) != 0) {
        printf("FAIL: deterministic — signatures differ\n");
        failures++;
        return;
    }

    printf("PASS: deterministic nonce\n");
}

int test_mweb_schnorr(void)
{
    failures = 0;

    /* Fixed-vector tests: byte-exact signature match */
    test_fixed_vector("scan_key + zero_msg",    SCAN_KEY,  MSG_ZERO, 32, SIG_SCAN_ZERO);
    test_fixed_vector("scan_key + nonzero_msg", SCAN_KEY,  MSG_NONZERO, 32, SIG_SCAN_NONZERO);
    test_fixed_vector("spend_key + zero_msg",   SPEND_KEY, MSG_ZERO, 32, SIG_SPEND_ZERO);
    test_fixed_vector("spend_key + nonzero_msg",SPEND_KEY, MSG_NONZERO, 32, SIG_SPEND_NONZERO);

    /* Determinism */
    test_deterministic();

    printf("\nmweb_schnorr: %d tests, %d failures\n", 5, failures);
    return failures;
}
